    private void formWindowOpened(java.awt.event.WindowEvent evt) {
        try {
            List<Book> books = bookController.getAllBooks();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("TÃ­tulo");
            model.addColumn("Autor");

            for (Book book : books) {
                String authorName = book.getAuthor() != null ?
                        book.getAuthor().getFullName().getFirstName() + " " + book.getAuthor().getFullName().getLastName()
                        : "";
                model.addRow(new Object[]{book.getId(), book.getTitle().getValue(), authorName});
            }

            bookTable.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al cargar libros: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

