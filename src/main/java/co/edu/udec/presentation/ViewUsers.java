    private void buttonSearch1ActionPerformed(java.awt.event.ActionEvent evt) {
        String email = emailField.getText().trim();

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un email para buscar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            Optional<User> result = controller.findUserByEmail(email);

            if (result.isPresent()) {

                User user = result.get();

                nameField.setText(user.getFullName().getFirstName());
                middleNameField.setText(user.getFullName().getMiddleName());
                lastNameField.setText(user.getFullName().getLastName());
                secondSurnameField.setText(user.getFullName().getSecondSurname());
                emailField.setText(user.getEmail().getValue());

                emailField.setEditable(false);

                buttonEdit.setEnabled(true);
                buttonDelete.setEnabled(true);

            } else {
                JOptionPane.showMessageDialog(this, "No se encontró ningún usuario con ese email.", "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error buscando usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {
        String email = emailField.getText().trim();

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Ingrese un email para eliminar.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            Optional<User> result = controller.findUserByEmail(email);

            if (result.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "No se encontró ningún usuario con ese email.",
                        "Sin resultados",
                        JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            User user = result.get();

            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "¿Está seguro de que desea eliminar al usuario:\n" +
                            user.getFullName().getFirstName() + " " +
                            user.getFullName().getLastName() + "?\n\nEsta acción no se puede deshacer.",
                    "Confirmar eliminación",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE
            );

            controller.deleteUser(user.getId());

            JOptionPane.showMessageDialog(this,
                    "Usuario eliminado correctamente.",
                    "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);

            emailField.setText("");
            nameField.setText("");
            middleNameField.setText("");
            lastNameField.setText("");
            secondSurnameField.setText("");
            passwordField.setText("");

            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error buscando usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

