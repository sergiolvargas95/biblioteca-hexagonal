    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {
        //buttonSearch1ActionPerformed(evt);

        String email = emailField.getText().trim();
        Optional<User> result = controller.findUserByEmail(email);

        if (result.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "No existe un usuario con ese email para editar.",
                    "Usuario no encontrado",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        User user = result.get();

        try {
            String firstName  = nameField.getText().trim();
            String middleName = middleNameField.getText().trim();
            String lastName   = lastNameField.getText().trim();
            String secondSurname = secondSurnameField.getText().trim();
            char[] passwordChars = passwordField.getPassword();
            String password = String.valueOf(passwordChars);

            if (firstName.isEmpty() || lastName.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Los campos de nombre y apellido son obligatorios.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            FullName fullNameVO = new FullName(
                    firstName,
                    middleName,
                    lastName,
                    secondSurname
            );

            user.setFullName(fullNameVO);

            if (!password.isBlank()) {
                user.setPassword(password);
            }

            controller.updateUser(user);

            JOptionPane.showMessageDialog(this,
                    "Usuario actualizado correctamente.",
                    "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al actualizar: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    private void buttonSaveActionPerformed(java.awt.event.ActionEvent evt) {
        String firstName = nameField.getText().trim();
        String middleName = middleNameField.getText().trim();
        String lastName = lastNameField.getText().trim();
        String secondSurname = secondSurnameField.getText().trim();
        String email = emailField.getText().trim();
        char[] passwordChars = passwordField.getPassword();
        String password = String.valueOf(passwordChars);

        if (firstName.isEmpty() || lastName.isEmpty() || email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe completar los campos obligatorios: Nombre, Apellido, Email y Contraseña.",
                    "Campos incompletos",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        String result = controller.createUser(
                firstName,
                middleName,
                lastName,
                secondSurname,
                email,
                password
        );

        JOptionPane.showMessageDialog(this, result);

        Arrays.fill(passwordChars, ' ');

        if (result.startsWith("Usuario creado")) {
            this.dispose();
            buttonEdit.setEnabled(false);
            buttonDelete.setEnabled(false);
        }
    }

